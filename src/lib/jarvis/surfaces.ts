export function listAtlasSurfaces() {
  return ATLAS_SURFACE_REGISTRY;
}

export function listAtlasAuditableSurfaces() {
  return ATLAS_SURFACE_REGISTRY.filter((surface) => surface.auditEnabled !== false && surface.auditPath);
}

export function getAtlasSurfaceByRoute(route: string) {
  return ATLAS_SURFACE_REGISTRY.find((surface) => surface.route === route || surface.auditPath === route) ?? null;
}

export function getAtlasJourneyRoutes() {
  return Array.from(new Set(listAtlasAuditableSurfaces().map((surface) => surface.auditPath!)));
}
