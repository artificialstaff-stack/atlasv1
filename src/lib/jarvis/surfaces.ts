import type { AtlasSurfaceContract } from "./types";

type SurfaceInput = Omit<AtlasSurfaceContract, "auditEnabled"> & {
  auditEnabled?: boolean;
};

function buildSurface(input: SurfaceInput): AtlasSurfaceContract {
  return {
    ...input,
    auditPath: input.auditPath ?? (input.route.includes("[") || input.route.includes("#") ? null : input.route),
    auditEnabled: input.auditEnabled ?? !input.route.includes("#"),
  };
}

function getAuditCustomerId() {
  return process.env.ATLAS_JARVIS_AUDIT_CUSTOMER_ID?.trim() || "6a3e8856-ef67-4ec1-a856-b92f53795ea1";
}

function getOptionalAuditPath(prefix: string, value: string | undefined) {
  const trimmed = value?.trim();
  return trimmed ? `${prefix}${trimmed}` : null;
}

